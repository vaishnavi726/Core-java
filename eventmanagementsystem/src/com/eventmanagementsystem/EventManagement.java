package com.eventmanagementsystem;
// implementing inheritance using this class by adding common properties of all classes

public class EventManagement {
	 protected Long id;
	 protected String name;
	
	 public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	 
	 

}
